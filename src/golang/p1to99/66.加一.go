// @lc code=start
func plusOne(digits []int) []int {
	reverse := func(arr []int) {
		for i, j := 0, len(arr)-1; i < j; i, j = i+1, j-1 {
			arr[i], arr[j] = arr[j], arr[i]
		}
	}

	reverse(digits)
	carry := 1
	for i, v := range digits {
		carry += v
		digits[i] = carry % 10
		carry /= 10
	}

	if carry > 0 {
		digits = append(digits, carry)
	}
	reverse(digits)

	return digits
}

